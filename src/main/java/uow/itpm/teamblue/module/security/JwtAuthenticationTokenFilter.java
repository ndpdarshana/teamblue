package uow.itpm.teamblue.module.security;

import org.springframework.http.HttpStatus;
import org.springframework.security.core.Authentication;
import org.springframework.security.core.AuthenticationException;
import org.springframework.security.web.authentication.AbstractAuthenticationProcessingFilter;
import uow.itpm.teamblue.model.JwtAuthenticationToken;

import javax.servlet.FilterChain;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.io.IOException;

/**
 * JwtAuthenticationTokenFilter used to authenticate filtered requests
 */
//@Component
public class JwtAuthenticationTokenFilter extends AbstractAuthenticationProcessingFilter {

    public JwtAuthenticationTokenFilter() {
        super(SecurityConstants.defaultFilterProcessUrl);
    }

    /**
     * Token send to validation within this method
     * @param request
     * @param response
     * @return
     * @throws AuthenticationException
     * @throws IOException
     * @throws ServletException
     */
    @Override
    public Authentication attemptAuthentication(HttpServletRequest request, HttpServletResponse response) throws AuthenticationException, IOException, ServletException {

        String token = request.getHeader("Authorization");
        if(token == null || !token.startsWith("Token ")){
            throw new RuntimeException("Authorisation token is missing");
        }

        token = token.substring(6);
        JwtAuthenticationToken jwtAuthenticationToken = new JwtAuthenticationToken(HttpStatus.ACCEPTED,token);
        return getAuthenticationManager().authenticate(jwtAuthenticationToken);
    }

    /**
     * When authenticated this method get called.
     * @param request
     * @param response
     * @param chain
     * @param authResult
     * @throws IOException
     * @throws ServletException
     */
    @Override
    protected void successfulAuthentication(HttpServletRequest request, HttpServletResponse response, FilterChain chain, Authentication authResult) throws IOException, ServletException {
        super.successfulAuthentication(request, response, chain, authResult);
        chain.doFilter(request, response);
    }
}
